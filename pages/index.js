import Head from "next/head";
import React, { useMemo, useState } from 'react';
import T1 from '../components/ver1/t1';
import T2 from '../components/ver1/t2';
import T3 from '../components/ver1/t3';

export default function Home() {
  const [selectedView, setSelectedView] = useState('t1');

  const componentMap = useMemo(() => ({
    t1: T1,
    t2: T2,
    t3: T3,
  }), []);

  const ActiveComponent = componentMap[selectedView];
  const buttons = [
    { id: 't1', label: 'T1' },
    { id: 't2', label: 'T2' },
    { id: 't3', label: 'T3' },
  ];

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      <main style={{ display: 'flex', flexDirection: 'column', height: '100vh', width: '100vw', backgroundColor: '#ffffff' }}>
        <div style={{ flex: 1, position: 'relative', display: 'flex', alignItems: 'stretch', justifyContent: 'center' }}>
          {ActiveComponent ? <ActiveComponent /> : <div style={{ color: '#999' }}>Select a view</div>}
        </div>
        <div style={{ padding: '18px', borderTop: '1px solid #eee', display: 'flex', justifyContent: 'center', gap: '12px', backgroundColor: '#ffffff' }}>
          {buttons.map(({ id, label }) => (
            <button
              key={id}
              onClick={() => setSelectedView(id)}
              style={{
                padding: '12px 20px',
                fontSize: '16px',
                cursor: 'pointer',
                backgroundColor: selectedView === id ? '#0070f3' : '#f0f0f0',
                color: selectedView === id ? '#ffffff' : '#000000',
                border: 'none',
                borderRadius: '6px',
                minWidth: '110px',
                transition: 'background-color 0.2s ease',
              }}
            >
              {label}
            </button>
          ))}
        </div>
      </main>
    </>
  );
}
